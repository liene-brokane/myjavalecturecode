
public class TaskVar {

	public static void main(String[] args) {
		int numAdd;
		numAdd = 125 + 24;
		System.out.println(numAdd + " = 125 + 24");
		int numSub;
		numSub = 125 - 24;
		System.out.println(numSub + " = 125 - 24");
		int numMult;
		numMult = 125 * 24;
		System.out.println(numMult + " = 125 x 24");
		int numDiv;
		numDiv = 125 / 24;
		System.out.println(numDiv + " = 125 % 24");
		int numMod;
		numMod = 125 % 24;
		System.out.println(numMod + " = 125 mod 24");
	}

}
